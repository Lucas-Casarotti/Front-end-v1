

// Operadores de comparação

var n1 = 10;
var n2 = "10";

//igual 
console.log(n1 == n2); //true
console.log(n1 === n2) //false (compara o tipo)

//diferente
console.log(n1 != n2); //false
console.log(n1 !== n2) //true (compara o tipo)

// maior
console.log(n1 > n2)  //false

// maior ou igual 
console.log(n1 >= n2) //true

// menor
console.log(n1 < n2)  //false

// menor ou igual 
console.log(n1 <= n2) //true